pub fn display_room(room_index: i32) -> CompactString {
    let room_id = try_get_room_id(room_index);
    let chapter_id = room_index / 10000;
    let room_index = room_index % 10000;
    let room_name = try_get_room_name(chapter_id, room_index);

    match (room_index, room_id, room_name) {
        (index, Some(id), Some(name)) => format_compact!("{name} ({index}. {id})"),
        (index, Some(id), _) => format_compact!("{id} ({index})", id = id),
        (index, _, _) => format_compact!("Unknown room {index}", index = index),
    }
}
